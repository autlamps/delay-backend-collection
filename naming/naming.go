// GetRandomName returns a random name from the list of animals and adjectives in the format
// "adjective-animal-randomnumber"
func GetRandomName() string {
	rand.Seed(time.Now().UnixNano())
	return fmt.Sprintf("%s-%s-%v", left[rand.Intn(len(left))], right[rand.Intn(len(right))], rand.Intn(1000))
}
